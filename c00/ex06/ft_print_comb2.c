#include <unistd.h>

void	ft_print_comb2()
{
	char	two_nums[5];
	int 	a;
	int 	b;

	two_nums[2] = ' ';
	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			two_nums[0] = '0' + (a / 10);
			two_nums[1] = '0' + (a % 10);
			two_nums[3] = '0' + (b / 10);
			two_nums[4] = '0' + (b % 10);
			write(1, two_nums, 5);
			if (a != 98 || b != 99)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
